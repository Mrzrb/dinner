async function main() {
  console.log(`Seeding ${rawRecipes.length} recipes...`);

  let count = 0;
  for (const [cat, name, main, side, seasoning, stepsRaw] of rawRecipes) {
    const category = categoryMap[cat] || "OTHER";
    const ingredients = parseIngredients(main, side, seasoning);
    const steps = parseSteps(stepsRaw);

    await prisma.recipe.create({
      data: {
        name,
        category,
        ingredients,
        steps,
        familyId: FAMILY_ID,
        createdBy: CREATED_BY,
      },
    });
    count++;
    console.log(`  [${count}] ${name} (${cat} → ${category})`);
  }

  console.log(`\nDone! ${count} recipes created.`);
  await prisma.$disconnect();
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
