type Category = "MEAT" | "VEGETABLE" | "SOUP" | "STAPLE" | "SNACK" | "OTHER";

interface RecipeData {
  name: string;
  category: Category;
  ingredients: { name: string; amount: string; unit: string }[];
  steps: { order: number; content: string }[];
}

function parseIngredients(
  main: string,
  side: string,
  seasoning: string
): { name: string; amount: string; unit: string }[] {
  const items: { name: string; amount: string; unit: string }[] = [];
  const all = [main, side, seasoning].filter(Boolean).join("，");
  const parts = all.split(/[，、]/);
  for (const p of parts) {
    const trimmed = p.trim();
    if (trimmed) {
      items.push({ name: trimmed, amount: "", unit: "" });
    }
  }
  return items;
}

function parseSteps(text: string): { order: number; content: string }[] {
  const steps: { order: number; content: string }[] = [];
  // Split by step numbers like "1、" "2、"
  const parts = text.split(/(?=\d+、)/);
  let order = 1;
  for (const p of parts) {
    const trimmed = p.replace(/^\d+、\s*/, "").trim().replace(/[；;]\s*$/, "");
    if (trimmed) {
      steps.push({ order, content: trimmed });
      order++;
    }
  }
  return steps;
}

